using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;
using TMPro;
public class PlayerAttributesManager : AttributesManager
{
    public HealthBar healthBar;
    public Slider xpSlider;
    public TextMeshProUGUI levelText;
    public LevelUpUI levelUpUI;

    private PlayerAnimation playerAnimation;

    public int currentLevel = 1;
    public int currentXP = 0;
    public int xpToNextLevel = 100;

    private List<Ability> specialAblities = new List<Ability>();

    // Start is called before the first frame update
    protected override void Start()
    {
        base.Start();
        healthBar.SetMaxHealth(maxHealth);
        healthBar.SetHealth(maxHealth);
        UpdateXPUI();

        playerAnimation = GetComponent<PlayerAnimation>();
    }

    // Damage부분
    protected override void TakeDamage(int damage)
    {
        base.TakeDamage(damage);
        healthBar.SetHealth(_health);
    }

    protected override void Die()
    {
        playerAnimation.Dead();
    }

    // XP부분
    protected override void GainXP(int xp)
    {
        currentXP += xp;
        if (currentXP >= xpToNextLevel)
        {
            LevelUp();
            UpdateXPUI();
        }
        UpdateXPUI();
    }

    public void LevelUp()
    {
        currentLevel++;
        currentXP -= xpToNextLevel;
        xpToNextLevel = Mathf.RoundToInt(xpToNextLevel * 1.5f);
        maxHealth += 10;
        _health = maxHealth;
        _attack += 5;

        healthBar.SetMaxHealth(maxHealth);
        healthBar.SetHealth(_health);

        // 레벨업 UI 표시
        if(levelUpUI != null)
        {
            Debug.Log("레벨업UI");
            levelUpUI.ShowLevelUpOptions();
        }
    }

    public void IncreaseStat(string statName, int amount)
    {
        switch (statName)
        {
            case "strength":
                _attack += amount;
                break;
            case "agility":
                // 예시로 민첩성 추가
                break;
            case "intelligence":
                // 예시로 지능 추가
                break;
        }
    }

    public void AddAbility(Ability ability)
    {
        specialAblities.Add(ability);
    }


    public void UpdateXPUI()
    {
        if (xpSlider != null)
        {
            xpSlider.maxValue = xpToNextLevel;
            xpSlider.value = currentXP;
        }

        if (levelText != null)
        {
            levelText.text = $"Level : {currentLevel} ( {currentXP} / {xpToNextLevel} )";
        }
    }
}
